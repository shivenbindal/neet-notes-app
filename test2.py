import os
import time
from flask import Flask, request, jsonify
from flask_cors import CORS
import requests as http

app = Flask(__name__)
CORS(app)

OPENROUTER_KEY = os.environ.get("OPENROUTER_KEY")

@app.route("/")
def home():
    return open(os.path.join(os.path.dirname(__file__), "index.html")).read()

@app.route("/generate", methods=["POST"])
def generate():
    topic = request.json["topic"]
    prompt = f"""Create visually stunning and comprehensive study notes on the topic: {topic}

Return ONLY a complete HTML page with these requirements:

1. Dark theme: background #0f0f13, cards #1a1a24, text #e8e8f0
2. A clean header with the topic name in purple (#7F77DD)
3. Content split into clearly styled sections with colored headers
4. Key terms highlighted in purple boxes
5. Important facts in glowing bordered callout boxes
6. Tables where relevant with dark styling
7. A quick-revision summary box at the end
8. Footer saying Generated by ur notes buddy
9. Make it detailed, exam-focused, and cover all subtopics thoroughly
10. Add a Download as PDF button fixed at top-right styled in purple that calls window.print() when clicked. Also add print CSS that hides the button when printing.
11. Add a back button at top-left that says back to Home and links back to /

Return complete HTML only, no explanation."""

    response = http.post(
        "https://openrouter.ai/api/v1/chat/completions",
        headers={
            "Authorization": f"Bearer {OPENROUTER_KEY}",
            "Content-Type": "application/json"
        },
        json={
            "model": "meta-llama/llama-3.2-3b-instruct:free",
            "max_tokens": 10000,
            "messages": [{"role": "user", "content": prompt}]
        }
    )

    result = response.json()
    if "choices" not in result:
        return jsonify({"error": str(result)}), 500
    html_output = result["choices"][0]["message"]["content"]
    html_output = html_output.replace("```html", "").replace("```", "")
    return jsonify({"html": html_output})

if __name__ == "__main__":
    app.run(host="0.0.0.0", port=int(os.environ.get("PORT", 5000)))
